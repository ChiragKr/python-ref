# basic arithmetic operators

x = 5 + 3
print(x) #--> 8
x = 15 - 5
print(x) #--> 10
x = 4 * 3
print(x) #--> 12

x = 15/4 # float division
print(x) #--> 3.75
x = 15//4 # integer division
print(x) #--> 3

x = 4**3
print(x) #--> 64
x = pow(4, 3)
print(x) #--> 64

